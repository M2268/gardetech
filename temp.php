

Ułatw sobie pracę w ogrodzie, wybierając plecakowy opryskiwacz spalinowy o pojemności 12 litrów 2.9KM. Możesz wykorzystać go do pielęgnacji roślin uprawnych, drzew i krzewów - w ogrodach, sadach, parkach. Doskonale sprawdza się jako sposób na rozpylenie środków ochrony przed szkodnikami i chorobami.

Stanowi najlepszą alternatywę opryskiwacza ręcznego

Od teraz nie musisz korzystać z ręcznych urządzeń - mniej wydajnych i niewygodnych. Dzięki temu praca przebiegnie znacznie sprawniej i nie będzie tak męcząca. Obsługa jedną ręką znacznie ułatwia wszelkie prace ogrodnicze.

Zasilanie silnikiem spalinowym

Takie rozwiązanie umożliwia korzystanie z opryskiwacza z dala od źródeł energii - przede wszystkim na otwartej przestrzeni.

Wygodny system nośny

Dzięki temu, że opryskiwacz ma formę plecaka, jest niezwykle wygodny w użytkowaniu. Ciężar rozkłada się równomiernie, dzięki czemu nie czujesz obciążenia. Ponadto ergonomiczny kształt zapewnia maksymalny komfort podczas użytkowania, stanowiąc oparcie pleców.

Solidna i wytrzymała obudowa<br>

Obudowa opryskiwacza stanowi połączenie tworzywa sztucznego i stopu aluminium. Nadaje mu lekkości i jest łatwy do przenoszenia, a przy tym odporny na rdzewienie. Pozwala również na łatwe utrzymanie pojemnika w czystości.<br>

Kilka wariantów dysz zraszających oraz opcja regulacji ciśnienia<br>

Umożliwia to opryskiwanie roślin różnych gatunków (delikatnych i silnych) po wyborze odpowiedniej dyszy oraz uregulowaniu strumienia oprysku.<br>

Duży zasięg opryskugarde<br>

Dlatego pozwoli na dokładne zraszanie nawet najdalej posadzonych roślin, do których opryskiwacz ręczny nie miałby dostępu.<br>

Wysoka wydajność<br>

Zużywa ok. 2 litry płynu w ciągu minuty pracy, dzięki czemu opryskiwanie zajmuje znacznie mniej czasu i jest dokładniejsze.<br>

Niskie zużycie paliwa<br>

Nie musisz obawiać się dużych strat paliwa. Zaledwie 0,5 litra paliwa wystarczy Ci na 1 godzinę pracy.<br>

Dzięki opryskiwaczowi spalinowemu 2.9KM przekonasz się, że praca w ogrodzie może być jeszcze większą przyjemnością. Bez zbędnej straty czasu i pieniędzy, lecz wydajnie i efektywnie.<br>


